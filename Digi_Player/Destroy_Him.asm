SID = $D400             ; base address of SID chip
NMIDIGIPTR = $FB        ; $FB $FC zero page pointer for sample data start
NMIPOINT   = $FD        ; $FD $FE zero page pointer to current sample byte 
 

                LDA #$35                ; turn off kernel
                STA $01                 ; while playing

                JSR NMI_Init            ; initialize NMI
                LDY #<params            ; grab address of sample start/end
                LDX #>params            ; address and NMI rate
                JSR DIGI_Play           ; now call play to start NMI player

                RTS                     ; set up done, NMI player takes over


; Initialize SID chip and CIA#2 NMI interrupt
NMI_Init 
                jsr NMI_Off             ; turn off NMI

                lda #$00                ; zeros out all SID registers
                ldx #$00                ;
@SIDCLR         sta SID,x               ; 
                inx                     ;
                bne @SIDCLR             ;

                ; set up SID voices so they will be modulated too
                ; this increases volumn on 8580 SIDs
                lda #$00                ; 
                sta SID+$05             ; voice 1 ad 
                lda #$F0                ;
                sta SID+$06             ;         sr 
                lda #$01                ;
                sta SID+$04             ;         ctrl 
                lda #$00 
                sta SID+$0C             ; voice 2 ad 
                lda #$F0                ;
                sta SID+$0D             ;         sr 
                lda #$01                ;
                sta SID+$0B             ;         ctrl 
                lda #$00 
                sta SID+$13             ; voice 3 ad 
                lda #$F0                ;
                sta SID+$14             ;         sr 
                lda #$01                ;
                sta SID+$12             ;         ctrl 
                lda #$00 
                sta SID+$15             ; filter  lo 
                lda #$10                ;
                sta SID+$16             ; filter  hi 
                lda #%11110111          ;
                sta SID+$17             ; filter  voices+reso 

                rts 


; Turn on NMI so we can play a sample
NMI_On 
                LDA #>NMIDIGION         ; set the NMI handler address
                STA $FFFB               ; high byte
                LDA #<NMIDIGION         ;
                STA $FFFA               ; low byte

                LDA #%10000001          ; enable CIA-2 timer A NMI 
                STA $DD0D               ; ICR
                lda $DD0D               ;
                LDA #%00000001          ; timer A start 
                STA $DD0E               ;

                rts 


; turn off NMI as we are done playing sample
NMI_Off 
                LDA #%00000000          ; turn off NMI
                STA $DD0E               ; timer A stop-CRA, CIA #1 DC0E
                LDA #%01001111          ; disable all CIA-2 nmi's 
                STA $DD0D               ; ICR - interrupt control / status
                lda $DD0D               ; sta/lda to ack any pending int

                LDA #>NMIDIGIOFF        ; NMI vector
                STA $FFFB               ; 
                LDA #<NMIDIGIOFF        ; sets interrupt vector to point to
                STA $FFFA               ; RTI statement at end of code

                lda #$00                ; zero out pointers
                sta NMIPOINT            ; NMIPOINT is current sample byte
                sta NMIPOINT+1          ;
                sta DIGISTOPLO+1        ; DIGISTOPHI/LO end address of sample
                sta DIGISTOPHI+1        ;

                rts 


; Called to start playing a digi sample
; notes are parameter passing here
DIGI_Play 
                jsr NMI_Off 

                stx NMIDIGIPTR+1        ; address of paramters passed in X,Y
                sty NMIDIGIPTR          ; save address to ZP @ $FC $FB

                ldy #$00 
                lda (NMIDIGIPTR),y      ; set current sample address pointer
                sta NMIPOINT            ; to start of sample
                iny     
                lda (NMIDIGIPTR),y 
                sta NMIPOINT+1          ; NMIPOINT is current sample byte

                iny 
                lda (NMIDIGIPTR),y      ; set end address of sample
                sta DIGISTOPLO+1        ; in code below
                iny 
                lda (NMIDIGIPTR),y      ; sets end point in code below
                sta DIGISTOPHI+1        ; DIGISTOPHI/LO end address of sample

                iny 
                lda (NMIDIGIPTR),y      ; sets CIA timer delay
                sta $DD04               ; TA LO
                iny 
                lda (NMIDIGIPTR),y 
                sta $DD05               ; TA HI

                jsr NMI_On 

                rts 


;------------------------------------------- 
; NMI calls here to play one 4-bit sample

NMIDIGION 
                STA NMIABUFF            ; saves A and Y registers in self
                STY NMIYBUFF            ; modifying code at exit (silly)

                lda #$10                ; #$00 gets dynamically changed 
D418NMI         ora #$00                ; *** when next sample read in
                sta SID+$18             ; volume reg 
                sta $D020               ; screen border color change

                LDA NMIPOINT+1          ; Current sample high byte
DIGISTOPHI      CMP #$12                ; ENDHIGH why #$12???
                BNE SK1                 ;

                LDA NMIPOINT            ; current sample low byte
DIGISTOPLO      CMP #$00                ; ENDLOW 
                BNE SK1 

                lda #$00                ; changes byte after ORA above ***
                STA D418NMI+1           ; preparing for next time used?
                jsr NMI_Off 
                LDA #$37                ; reset kernal banking
                STA $01                 ;
                LDA NMIABUFF            ; restores A register
                RTI                     ; doneplaying sample

SK1 
                LDY #$00                ; not done wtih sample yet

nib             LDA #$00                ; *** gets changed dynamically below 
                AND #$01                ; why are we looking for the low bit??
                BNE S1                  ; this is hi/low nibble logic!

                LDA (NMIPOINT),Y        ; reads next sample
                AND #$0F                ; low nibble first
                JMP S2                  ;

S1              LDA (NMIPOINT),Y        ; shifts hi nibble down
                lsr a
                lsr a
                lsr a
                lsr a                   ;

                INC NMIPOINT            ; inc the sample pointer
                BNE @SK 
                INC NMIPOINT+1 
@SK 
S2              
                STA D418NMI+1 
                INC nib+1               ; *** changes byte after LDA above
                LDA $DD0D               ; ack NMI interrupt


NMIABUFF=*+1 
                LDA #$00                ; the #$is modified at start
NMIYBUFF=*+1 
                LDY #$00                ; the #$is modified at start

NMIDIGIOFF 
                RTI 


